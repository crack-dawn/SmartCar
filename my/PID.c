/**
 * @brief 
 * 
 * @param Lout ¦Ë??????
 * @param Lerr ¦Ë?????
 * @param minV ??§³?speed
 * @param maxV ????speed
 * @return float 
 */
float Limit_Amplitude(float Lout, float Lerr,float minV, float maxV)
{
		// float distance = Abs(Lerr);
		Lout = 0.1*Lout;
		if( Abs(Lout) > maxV) // too more
		{
			return (Lout>0)?(maxV):(-maxV);
		}
		else if( Abs(Lout) < minV ) // too min
		{
			return (Lout>0)?(minV):(-minV);
		}
		else // mormal
		{
			return Lout;
		}
}





